     private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tbl_SP.getModel();
        model.setRowCount(0);
        try {
            List<SanPham> list = dao.selectAll();
            for (SanPham sp : list) {
                Object[] row = {
                    sp.getMASP(), sp.getTENSP(), sp.getGIA(), sp.getSOLUONG(), sp.getMOTA(), sp.getHINH(), sp.getHINH1(), sp.getHINH2(), sp.getHINH3()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            until.DialogHelper.alert(this, "Lỗi truy vấn dữ liệu");
        }
     }
