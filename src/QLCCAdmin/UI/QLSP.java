    void edit(){
        String mand = (String) tbl_SP.getValueAt(this.row, 0);
        SanPham nd = dao.selectById(mand);
        this.setForm(nd);
        tabs.setSelectedIndex(0);
        this.updateStatus();
        
    }
    
    void first(){
        this.row = 0;
        this.edit();
    }
    void prev(){
        if(this.row > 0){
            this.row --;
            this.edit();
        }
    }
    void next(){
        if(this.row < tbl_SP.getRowCount() - 1){
            this.row ++;
            this.edit();
        }
    }
    void last(){
        this.row = tbl_SP.getRowCount() - 1;
        this.edit();
    }
    JFileChooser fileChooser= new JFileChooser();
    private void chonAnh() {
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            XImage.save(file);
            try {
                lblHinh.setIcon(XImage.readAndResize(lblHinh, file.getName()));
            } catch (IOException ex) {
                DialogHelper.alert(this, "Lỗi tải hình ảnh");
            }
            lblHinh.setToolTipText(file.getName());
        }
    }
